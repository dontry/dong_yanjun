    private void addTableModel(DefaultTableModel dcbm){
        if (dcbm.getRowCount() > 0) {
            /* clear the default table model */
            for (int i = dcbm.getRowCount() - 1; i > -1; i--) {
                dcbm.removeRow(i);
            }
        }
    }

