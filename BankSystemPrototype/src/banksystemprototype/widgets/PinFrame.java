    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        String pin = DataConverter.charArraysToString(tfPin.getPassword());
        getPin(pin, new Callback() {
            @Override
            public void onload(Object pin) {
                isVerified = mListener.verifyPin(pin);
            }
            
        });
        if(isVerified) {
            this.dispose();
            jLabel2.setText("");
            chances = 3;
        } else {
            if(--chances <= 0) {
                mListener.freezeAccount();
                this.dispose();
            };            
            String msg = "Sorry, your PIN is ivalid.\n You have " + chances + " chance(s) left."; 
            jLabel2.setText(msg);
            tfPin.setText("");
        }
        
    }//GEN-LAST:event_btnOKActionPerformed

